// keyboard device file discriptor
int input = 0;
// store terminal flag;
int term_flag = 0;

void init_input()
{
    // open keyboard device.
    input = open("/dev/input/event0", O_RDONLY);
    // disable terminal echo and control characters.
    termios termInfo;
    tcgetattr(0, &termInfo);
    term_flag = termInfo.c_lflag;
    termInfo.c_lflag = ICANON;
    termInfo.c_lflag &= ~ECHO;
    tcsetattr(0, TCSANOW, &termInfo);

}

void deinit_input()
{
    termios termInfo;
    tcgetattr(0, &termInfo);
    termInfo.c_lflag = term_flag;
    tcsetattr(0, TCSANOW, &termInfo);
    close(input);
}

bool handle_input()
{
    input_event event;
    if (read(input, &event, sizeof(event)) != sizeof(event)) {
        exit(EXIT_FAILURE);
    }
    if (event.type != EV_KEY) return false;

    const std::string key_name =
            code_names.count(event.code) ?
                code_names.at(event.code) :
                "KEY_UNKNOWN";

    const std::vector<std::string> values =
    { "RELEASED", "PRESSED", "AUTO-REPEAT" };

#ifndef NDEBUG
    std::cout << key_name << "\t" << values[event.value] << std::endl;
    // for debug, assign Hiragana/Katakana key to exit program.
    if (event.code == KEY_KATAKANAHIRAGANA) {
        deinit_input();
        exit(EXIT_SUCCESS);
    }
#endif
    return true;
}
